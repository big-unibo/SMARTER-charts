
export const binningColorConfig = function (value) {
    if (value == 6) {
        return 'rgb(5, 48, 97)';
    } else if (value == 5) {
        return 'rgb(51, 124, 183)';
    } else if (value == 4) {
        return 'rgb(143, 194, 221)';
    } else if (value == 3) {
        return 'rgb(241, 163, 133)'
    } else if (value == 2) {
        return 'rgb(195, 61, 61)';
    } else return 'rgb(140, 13, 37)'
};